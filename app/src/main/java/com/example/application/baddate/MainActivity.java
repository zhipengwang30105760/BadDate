        PendingIntent pendingIntent = PendingIntent.getBroadcast(this, 1, intent, 0);
        intent.setData(Uri.parse("tel:12312312"));//change the number

        if (c.before(Calendar.getInstance())) {
            c.add(Calendar.DATE, 1);
        }
        startActivity(intent);
        // phone call function works but I cannot schedule the phone call when the time exactly match the instance time.
